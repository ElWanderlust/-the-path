//Observations scribed or engraved 

$('#scribe').click(function() {
    let message = $('textarea').val();
    if (message != '') {
        alert(message);
    }
    else {
        alert('Message field left blank.');
    }

})

$('#engrave').click(function() {
    let message = $('textarea').val();
    let text = $('#observationBody').text();
    if (message != '') {
        text + message;
    }
    else {
        alert('Message field left blank.');
    }

})